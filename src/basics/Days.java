package basics;

public class Days {
	
	public static void main(String[] args) {
		
		String dayOfWeek = "Friday";
		
		switch (dayOfWeek) {
			case "Monday": 
				System.out.println("This is a Monday");
				break;
			case "Tuesday": 
				System.out.println("This is a Tuesday");
				break;
			case "Wendesday": 
				System.out.println("This is a Wendesday");
				break;
			case "Thusday": 
				System.out.println("This is a Thusday");
				break;
			case "Friday": 
				System.out.println("This is a Friday");
				break;
			case "Saturday": 
				System.out.println("This is a Saturday");
				break;
			case "Sunday": 
				System.out.println("This is a Sunday");
				break;
		}
		
	}

}
